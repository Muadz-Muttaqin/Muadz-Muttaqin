# Buatlah program yang meminta pengguna untuk memasukkan dua kata, lalu menggabungkan kedua kata tersebut menjadi satu string baru tanpa spasi.



k_pertama = input("Masukkan kata pertama: ") #input:Program ini dibuat untuk membuat variable dan inputan kata pertam dari user
k_kedua = input("Masukkan kata kedua: ") #input:Program ini dibuat untuk membuat variable dan inputan kata kedua dari user

proses = k_pertama + k_kedua #proses menggabungkan dari variabel kata pertama dan kata kedua dengan mengunakan tanda +

print(f"Hasil gabungan tampa sepasi: {proses}") #qutput: menampilkan hasil dari gabunggan kata pertama dan kata ke dua











