# uatlah program yang mengonversi suhu dari Celcius ke Fahrenheit. Rumus untuk konversi adalah sebagai berikut:
# [ \text{Fahrenheit} = (\text{Celcius} \times \frac{9}{5}) + 32 ]


celcius = float(input("Masukkan suhu dalam Celcius:")) #input:Program ini dibuat untuk membuat variable dan inputan celcius dari user

proses = (celcius * 9/5) + 32 #proses celcius dikali 9/5 dan di tambah degan 32 dan di buat jadi 1 variabel yaitu proses

print(f"Suhu dalam Fahrenheit adalah: {proses}") #qutput: menampilkan hasil dari variabel proses











