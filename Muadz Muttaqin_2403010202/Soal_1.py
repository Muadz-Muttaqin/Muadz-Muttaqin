# Buatlah program yang meminta pengguna untuk memasukkan panjang dan lebar suatu persegi panjang. 
# Program harus menghitung dan menampilkan luas dan keliling dari persegi panjang tersebut.



p = float(input("Masukkan panjang: "))#input:Program ini dibuat untuk membuat variable dan inputan panjang dari user
l = float(input("Masukkan lebar: "))#input:Program ini dibuat untuk membuat variable dan inputan lebar dari user

luas = p * l # untuk mengkalikan panjang dan lebar dan di buat jadi 1 variabel yaitu luas
keliling = 2 * (p + l)# program ini di buat untuk panjang dan lebar di tambah dan di kalikan 2 dan di buat jadi 1 variabel yaitu keliling

print(f"Luas persegi panjang: {luas}") #qutput: Program ini dibuat untuk menampilkan proses dari variabel luas 
print(f"Keliling persegi panjang: {keliling}") #qutput: program ini dibuat untuk menampilkan proses dari variabel keliling 







